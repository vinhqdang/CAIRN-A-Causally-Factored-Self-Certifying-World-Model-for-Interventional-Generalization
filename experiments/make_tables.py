"""Regenerate RESULTS.md from the result JSONs in results/.

Tables are never hand-edited; this script is the single source of truth
for the reported numbers.  Usage:  python experiments/make_tables.py
"""

from __future__ import annotations

import json
import os

RESULTS_DIR = os.path.join(os.path.dirname(os.path.dirname(
    os.path.abspath(__file__))), "results")
OUT = os.path.join(os.path.dirname(RESULTS_DIR), "RESULTS.md")

MODEL_LABELS = {
    "cairn": "CAIRN (learned graph)",
    "cairn_noinv": "CAIRN w/o L_inv (ablation)",
    "cairn_oracle": "CAIRN (oracle graph, upper bound)",
    "monolithic": "Monolithic WM",
    "ensemble": "Probabilistic ensemble (PETS-style)",
}


def load(name):
    path = os.path.join(RESULTS_DIR, name)
    if not os.path.exists(path):
        return None
    with open(path) as f:
        return json.load(f)


def fmt(x, nd=3):
    if x is None:
        return "--"
    if isinstance(x, float):
        return f"{x:.{nd}f}"
    return str(x)


def table(header, rows):
    out = ["| " + " | ".join(header) + " |",
           "|" + "|".join("---" for _ in header) + "|"]
    for r in rows:
        out.append("| " + " | ".join(str(c) for c in r) + " |")
    return "\n".join(out) + "\n"


def main():
    parts = ["# CAIRN — Synthetic-DBN Evaluation Results\n",
             "All tables are generated by `experiments/make_tables.py` "
             "from the JSONs in `results/` (produced by "
             "`experiments/run_all.py`). See `algorithm.md` section 5 for "
             "the research questions and `results/meta.json` for the "
             "ground-truth graph.\n"]

    meta = load("meta.json")
    if meta:
        parts.append(f"Setup: d={meta['d']} latent variables, "
                     f"m={meta['m']} action dims, seed={meta['seed']}, "
                     f"{meta['train_steps']} training steps.\n")

    rq1 = load("rq1.json")
    if rq1:
        parts.append("## RQ1 — Interventional generalization\n")
        parts.append(
            f"Training interventions were restricted to nodes "
            f"{rq1.get('do_train_nodes')}; **held-out** rows evaluate "
            "do-queries on nodes never intervened during training "
            "(compositional interventional generalization), **trained** "
            "rows evaluate do-targets seen in training (with held-out "
            "values).  Rollout error of the model-mean trajectory against "
            "the true conditional mean (RMSE), plus the structural split "
            "at horizon 5: error on descendants vs non-descendants of the "
            "intervened node, with the no-intervention reference for the "
            "non-descendant set.\n")
        for group, label in [("heldout", "Held-out do-targets"),
                             ("trained", "Trained do-targets")]:
            if not any(group in rec for rec in rq1["models"].values()):
                continue
            parts.append(f"**{label}:**\n")
            header = ["Model", "h=1", "h=3", "h=5", "h=10",
                      "desc@3", "non-desc@3", "non-desc@3 (no-do ref)"]
            rows = []
            for name, by_group in rq1["models"].items():
                rec = by_group.get(group)
                if not rec:
                    continue
                r = rec["rmse_by_horizon"]
                rows.append([MODEL_LABELS.get(name, name),
                             fmt(r[0]), fmt(r[2]), fmt(r[4]), fmt(r[9]),
                             fmt(rec["desc_rmse"]["3"]),
                             fmt(rec["nondesc_rmse"]["3"]),
                             fmt(rec["nondesc_rmse_no_intervention"]["3"])])
            parts.append(table(header, rows))

    rq2 = load("rq2.json")
    if rq2:
        parts.append("## RQ2 — Detection & localization\n")
        parts.append(
            f"Alarm level delta = {rq2['delta']}.  Exact-null Ville "
            "verification (gates fed PITs from the true environment "
            "conditional): fraction of gates ever alarmed = "
            f"**{fmt(rq2.get('oracle_null_alarm_fraction'))}** over "
            f"{rq2.get('oracle_null_streams', '--')} independent "
            f"{rq2.get('null_stream_steps', '--')}-step null streams "
            "(guarantee: <= delta).\n")
        parts.append("Null-stream alarm fractions (learned models):\n")
        rows = [[det, fmt(v["alarm_fraction"]), v["unit"]]
                for det, v in rq2["null"].items()]
        parts.append(table(["Detector", "Alarm fraction", "Unit"], rows))
        parts.append(
            "Shift scenarios (unannounced single/double-mechanism "
            "shifts; delay in steps, localization over the alarmed set "
            "within 300 steps):\n")
        header = ["Detector", "Median delay", "Mean delay", "Loc. F1",
                  "Loc. precision", "Loc. recall"]
        rows = []
        for det, v in rq2["detectors"].items():
            rows.append([det, fmt(v["median_delay"], 1),
                         fmt(v["mean_delay"], 1),
                         fmt(v["localization_f1"]),
                         fmt(v["localization_precision"]),
                         fmt(v["localization_recall"])])
        parts.append(table(header, rows))
        parts.append(
            f"Global (unlocalized) e-gate on the monolithic model: median "
            f"detection delay {fmt(rq2['global_egate_median_delay'], 1)} "
            "steps, localization undefined by construction.\n")
        if rq2.get("note"):
            parts.append(f"*{rq2['note']}*\n")

    rq3 = load("rq3.json")
    if rq3:
        parts.append("## RQ3 — Adaptation speed after a mechanism shift\n")
        parts.append(
            "1-step RMSE on the shifted nodes (held-out shifted-regime "
            "transitions) vs number of post-shift samples; recovery = "
            "first checkpoint at or below 1.1x the pre-shift RMSE.  All "
            "methods get the same stream and the same gradient budget.\n")
        cps = rq3["checkpoints"]
        for sc in rq3["scenarios"]:
            parts.append(f"Shift S={sc['S']} (gain {sc['gain']}), "
                         f"pre-shift RMSE {fmt(sc['pre_shift_rmse'])}, "
                         f"recovery threshold "
                         f"{fmt(sc['recovery_threshold'])}:\n")
            header = ["Method", "Nodes", "n=0"] + [f"n={c}" for c in cps] + \
                ["Samples to recovery"]
            rows = []
            for mname, m in sc["methods"].items():
                rows.append([mname, "shifted S"]
                            + [fmt(x) for x in m["rmse_curve"]]
                            + [fmt(m.get("samples_to_recovery"))])
                if "rmse_curve_all_nodes" in m:
                    rows.append([mname, "all (interference)"]
                                + [fmt(x) for x in m["rmse_curve_all_nodes"]]
                                + [""])
            parts.append(table(header, rows))
            parts.append(
                "The *all (interference)* rows show what adaptation does to "
                "the rest of the model: localized e-gate refits leave the "
                "seven healthy mechanisms untouched, while full fine-tuning "
                "degrades them catastrophically mid-adaptation.\n")

    rq4 = load("rq4.json")
    if rq4:
        parts.append("## RQ4 — Calibration of rollout intervals\n")
        hs = rq4["horizons"]
        parts.append("Empirical coverage of nominal 90% intervals "
                     "(in-distribution):\n")
        header = ["Model"] + [f"h={h}" for h in hs]
        rows = []
        for name, rec in rq4["nominal"].items():
            rows.append([MODEL_LABELS.get(name, name)]
                        + [fmt(rec["cov90"][str(h)]) for h in hs])
        parts.append(table(header, rows))
        parts.append("Under an unannounced mechanism shift "
                     "(coverage of nominal 90% intervals; the "
                     "*shifted node* columns score only the broken "
                     "mechanism's variable, where honesty is at stake):\n")
        header2 = ["Condition"] + [f"h={h}" for h in hs] + \
            [f"shifted node h={h}" for h in hs]
        rows = []
        for name, rec in rq4["shift"].items():
            row = [name] + [fmt(rec["cov90"][str(h)]) for h in hs]
            if "cov90_shifted_node" in rec:
                row += [fmt(rec["cov90_shifted_node"][str(h)]) for h in hs]
            else:
                row += ["--"] * len(hs)
            rows.append(row)
        parts.append(table(header2, rows))

    rq5 = load("rq5.json")
    if rq5:
        parts.append("## RQ5 — Downstream planning utility (CVaR-CEM)\n")
        parts.append(f"Goal-reaching returns (mean +- SEM over "
                     f"{rq5['episodes']} episodes):\n")
        header = ["Condition", "CAIRN", "Monolithic"]
        rows = []
        for cond, models in rq5["conditions"].items():
            c = models.get("cairn")
            m = models.get("monolithic")
            rows.append([cond,
                         f"{fmt(c['mean_return'], 1)} +- {fmt(c['sem'], 1)}"
                         if c else "--",
                         f"{fmt(m['mean_return'], 1)} +- {fmt(m['sem'], 1)}"
                         if m else "--"])
        parts.append(table(header, rows))

    rq6 = load("rq6.json")
    if rq6:
        parts.append("## RQ6 — Structure recovery vs regime diversity\n")
        header = ["Training regimes", "SHD(A)", "F1(A)", "SHD(M)", "F1(M)"]
        rows = []
        for n, sc in zip(rq6["regime_counts"], rq6["scores"]):
            rows.append([n, sc["shd_A"], fmt(sc["f1_A"]),
                         sc["shd_M"], fmt(sc["f1_M"])])
        parts.append(table(header, rows))

    with open(OUT, "w") as f:
        f.write("\n".join(parts))
    print(f"[saved] {OUT}")


if __name__ == "__main__":
    main()
